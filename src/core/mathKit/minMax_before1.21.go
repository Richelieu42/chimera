//go:build !go1.21

package mathKit

import (
	"golang.org/x/exp/constraints"
)

func Max[T constraints.Ordered](p T, args ...T) (max T) {
	max = p

	for _, ele := range args {
		if ele > max {
			max = ele
		}
	}
	return max
}

func Min[T constraints.Ordered](p T, args ...T) (min T) {
	rst := p

	for _, ele := range args {
		if ele < rst {
			rst = ele
		}
	}
	return rst
}
